graphConn = Null;

GraphQuery [predicates:{___Rule}, opts___Rule]:=
  GraphQuery[predicates,{"GRAPH"}, opts]

GraphQuery[predicates:{___Rule}, returnProps:{___String}, opts___Rule]:=
  Module[{showgraphs, maxhits, results, p, r = returnProps, x, headers},
    
    showgraphs = "ShowGraphs" /. Flatten[{opts}] /. Options[GraphQuery];
    maxhits = "MaxHits" /. Flatten[{opts}] /. Options[GraphQuery];
    If[!IntegerQ[maxhits], maxhits = 9];
    If[graphConn === Null, 
      graphConn = OpenSQLConnection["graphs"];
    ];
    
    x= (If[!MemberQ[GraphProperties[] , ToUpperCase[#]] , Return[$Failed]] & /@ returnProps);
    If[MemberQ[x, $Failed], Return[$Failed]];
    
    x=(If[!MemberQ[GraphProperties[] , ToUpperCase[First[#]]] , $Failed ] & /@ predicates);
    If[MemberQ[x, $Failed], Return[$Failed]];
    
    p = predicates /. {(Rule[col_String, val_])->Equal[SQLColumn[col], val]};
    
    If[showgraphs && !MemberQ[ToUpperCase/@returnProps, "GRAPH"] ,
      PrependTo[r, "GRAPH"];
    ];
    
    results = 
      SQLSelect[graphConn, "GRAPHS", r, And@@ p,
        "MaxRows"->maxhits, "ShowColumnHeadings"->True];
    
    If[TrueQ[showgraphs],
      headers=ToUpperCase/@results[[1]];
      {graphIndex} = Flatten[Position[headers,"GRAPH"]];
      results = (MapAt[ToExpression, #, graphIndex] & /@ Drop[results,1]);      
      results = ReplacePart[#, graphIndex-> ShowGraph[#[[graphIndex]]]]& /@ results
      ,
      If[MemberQ[ToUpperCase/@returnProps, "GRAPH"],
        headers=ToUpperCase/@results[[1]];
        {graphIndex}=Flatten[Position[headers,"GRAPH"]];
        results = (MapAt[ToExpression, #, graphIndex] & /@ Drop[results,1]), 
        results = Drop[results, 1];
      ]
    ];
    results
    ]
End[];

